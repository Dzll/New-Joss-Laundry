    private void selectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectActionPerformed
        // TODO add your handling code here:
        if(txnamapel.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Select Pelanggan","WARNING", JOptionPane.WARNING_MESSAGE);
        }else{
            if(txid.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Select Jenis Cucian","WARNING", JOptionPane.WARNING_MESSAGE);
            }else{
                s1.setText(txnamapel.getText()); s1a.setText(txidpel.getText());
                s2.setText(txalamatpel.getText());
                s3.setText(txnohppel.getText());
                s4.setText(txnama.getText()); s4a.setText(txid.getText());
                s5.setText(txharga.getText());

                spec.setVisible(true);
                select.setVisible(false);
                btntransaksi.setVisible(true);

                kodeload(txidpel);
                txnamapel.setText("");
                txalamatpel.setText("");
                txnohppel.setText("");
                txid.setText("");
                txnama.setText("");
                txharga.setText("");
                tablejenis(jTable1);
                tablepelanggan(jTable2);
            }
        }
        
    }//GEN-LAST:event_selectActionPerformed

