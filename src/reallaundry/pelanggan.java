    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "INSERT INTO pelanggan (id_pelanggan, nama_pelanggan, alamat_pelanggan, nohp_pelanggan) VALUES (?,?,?,?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            
            pst.setString(1, txidpel.getText().trim());
            pst.setString(2, txnamapel.getText().trim());
            pst.setString(3, txalamatpel.getText().trim());
            pst.setString(4, txnohppel.getText().trim());
            
            pst.execute(); 
            tablepelanggan(jTable2);
            visible();
            btnbaru.setText("Pelanggan Baru");
            JOptionPane.showMessageDialog(null, "Data disimpan");

            //new jenis_cucian().setVisible(true);
            //dispose();
            
        } catch (Exception ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } 
    }//GEN-LAST:event_btnsaveActionPerformed

